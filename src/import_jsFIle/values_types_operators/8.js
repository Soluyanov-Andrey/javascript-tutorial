/**
 * Глобальное свойство NaN является значением, представляющим не-число (Not-A-Number).
 *  undefined является не настраиваемым и не записываемым свойством,
 */
// Преобразует строковую "2" в 2 и умножит на 5.
console.log("2" * 5);

// Не сможет преобразовать "abc" в число и присвоит NaN не-число, NaN * 5 = NaN.
console.log("abc" * 5); // NaN

// Cвойство NaN в js имеет тип number.
console.log(typeof NaN); //number

// Если объявить необъявленное значение, то выдаст undefined.
console.log(typeof ddds);//undefined




